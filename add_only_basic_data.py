def get_date(start_time, interval):
    from datetime import datetime
    from datetime import timedelta
    c = []
    for i in range(interval):
        c.append((datetime.strptime(start_time, '%Y-%m-%d') + timedelta(days=i)).strftime("%Y-%m-%d"))
    return c


